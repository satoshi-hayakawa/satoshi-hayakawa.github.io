		// モーダル要素の取得
		const modal = document.getElementById('bibtexModal');
		const modalTitle = document.getElementById('bibtexTitle');
		const modalContent = document.getElementById('bibtexContent');
		const closeBtn = document.querySelector('.bibtex-close');
		const copyButton = document.getElementById('copyButton');

		// BibTeXボタンのクリックイベント
		document.addEventListener('click', function(e) {
			if (e.target.classList.contains('bibtex-button')) {
				e.preventDefault();
				const bibtexKey = e.target.getAttribute('data-bibtex');
				const bibtexContent = bibtexDatabase[bibtexKey];
				
				if (bibtexContent) {
					modalTitle.textContent = `BibTeX Citation`;
					modalContent.textContent = bibtexContent;
					modal.style.display = 'block';
					
					// コピーボタンをリセット
					copyButton.textContent = 'Copy to Clipboard';
					copyButton.classList.remove('copied');
				}
			}
		});

		// モーダルを閉じる
		closeBtn.addEventListener('click', function() {
			modal.style.display = 'none';
		});

		// モーダル外をクリックで閉じる
		window.addEventListener('click', function(e) {
			if (e.target === modal) {
				modal.style.display = 'none';
			}
		});

		// ESCキーでモーダルを閉じる
		document.addEventListener('keydown', function(e) {
			if (e.key === 'Escape' && modal.style.display === 'block') {
				modal.style.display = 'none';
			}
		});

		// コピー機能
		copyButton.addEventListener('click', async function() {
			try {
				await navigator.clipboard.writeText(modalContent.textContent);
				copyButton.textContent = 'Copied!';
				copyButton.classList.add('copied');
				
				setTimeout(() => {
					copyButton.textContent = 'Copy to Clipboard';
					copyButton.classList.remove('copied');
				}, 2000);
			} catch (err) {
				// フォールバック: テキストエリアを使用
				const textArea = document.createElement('textarea');
				textArea.value = modalContent.textContent;
				document.body.appendChild(textArea);
				textArea.select();
				document.execCommand('copy');
				document.body.removeChild(textArea);
				
				copyButton.textContent = 'Copied!';
				copyButton.classList.add('copied');
				
				setTimeout(() => {
					copyButton.textContent = 'Copy to Clipboard';
					copyButton.classList.remove('copied');
				}, 2000);
			}
		});